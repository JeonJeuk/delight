function filter() {
    let value, name, item, i;
    value = document.getElementById("value").value.toUpperCase();
    item = document.querySelectorAll(".names");
    let firstMatch = null;

    for (i = 0; i < item.length; i++) {
        name = item[i];
        if (name.innerHTML.toUpperCase().indexOf(value) > -1) {
            name.parentElement.parentElement.parentElement.style.display = "inline-block";

                        // 첫 번째 일치하는 요소를 찾은 경우
                        if (!firstMatch) {
                            firstMatch = name.parentElement.parentElement.parentElement;
                        }
        } else {
            name.parentElement.parentElement.parentElement.style.display = "none";
        }
    }

    // 첫 번째 일치하는 요소로 스크롤 이동
    if (firstMatch) {
        let firstMatchStart = $(firstMatch).offset().left;
        let parentStart = $startList.offset().left;
        let relativeScrollPos = firstMatchStart - parentStart;
        $startList[0].scrollLeft = relativeScrollPos;
    }

}

